    current = user_state.get(user_id, 0)
    next_idx = (current + 1) % len(offers)
    user_state[user_id] = next_idx
    await callback.message.edit_text(
        text=offers[next_idx]["text"],
        reply_markup=get_offer_keyboard(offers[next_idx]["id"])
    )
    await callback.answer()

